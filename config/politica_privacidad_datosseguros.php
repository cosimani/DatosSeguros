<h2>Política de privacidad</h2>

<p>Bienvenidos a DatosSeguros. </p>

<p>En nuestra Política de privacidad se describe cómo nosotros recopilamos, utilizamos y protegemos la información relacionada con esta aplicación.</p>

<h3>1. INFORMACIÓN RECOPILADA</h3>

<p>Recopilamos la siguiente información.

<ul>
	<li>Tu nombre de usuario, contraseña y dirección de correo electrónico al registrarte en una cuenta.</li>
	<li>Información de tu profesión y servicios ofrecidos.</li>
</ul>

</p>

<h3>2. CÓMO UTILIZAMOS TU INFORMACIÓN</h3>

<p>Además de algunos de los usos específicos de la información que describimos en esta Política de privacidad, podemos utilizar la información recibida para:

<ul>
	<li>Ayudarte a acceder eficazmente a la información después de registrarte.</li>
	<li>Desarrollar y poner a prueba nuevos productos y funciones.</li>
	<li>Supervisar los resultados de la aplicación.</li>
	<li>Diagnosticar o solucionar problemas tecnológicos.</li>
</ul>

</p>

<h3>3. USO COMPARTIDO DE TU INFORMACIÓN</h3>

<p>No procederemos al alquiler o venta de la información que nos proporciones a terceros sin tu consentimiento. 
</p>

<h3>4. CÓMO ALMACENAMOS TU INFORMACIÓN</h3>

<p>La información de tu propiedad que se recopila en nuestros servidores de base de datos.
</p>

<h3>5. CÓMO PONERTE EN CONTACTO CON NOSOTROS</h3>

<p>Si tienes alguna pregunta sobre esta Política de privacidad o sobre la aplicación, busca el canal de ayuda correspondiente en el servicio de ayuda en el que puedes ponerte en contacto con nosotros.
</p>

<h3>6. CAMBIOS EN NUESTRA POLÍTICA DE PRIVACIDAD</h3>

<p>Podemos modificar o actualizar esta Política de privacidad periódicamente, por lo que te aconsejamos que la revises de vez en cuando. 
</p>

